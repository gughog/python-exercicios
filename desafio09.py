# na tela sua tabuada.

num = int(input('Digite um número para obter sua tabuada... '))

print('A tabuada do {} corresponte a:'.format(num))

print('{} x 1 = {} \n {} x 2 = {} \n {} x 3 = {} \n {} x 4 = {} \n {} x 5 = {}'.format(num, num * 1, num, num * 2, num, num * 3, num, num * 4, num, num * 5))
print('{} x 6 = {} \n {} x 7 = {} \n {} x 8 = {} \n {} x 9 = {} \n {} x 10 = {}. '.format(num, num * 6, num, num * 7, num, num * 8, num, num * 9, num, num * 10))



